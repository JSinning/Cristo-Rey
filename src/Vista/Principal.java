    private void jButton_GuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton_GuardarMouseClicked
        String fecha = Convertirfecha().toString();
        String nombre_completo = jTextField_NombreCompleto.getText();
        String cedula = jTextField_Cedula.getText();
        int edad = Integer.parseInt(jTextField_Edad.getText());
        String direccion = jTextField_Direccion.getText();
        String telfono = jTextField_Telefono.getText();
        String profesion = jTextField_Profesion.getText();
        String estado = jComboBox_Estado.getSelectedItem().toString();
        //controller
        controller contro = new controller();
        //modelo
        registro registro = new registro(fecha, nombre_completo, cedula, edad, direccion, telfono, profesion, estado);
        contro.crear(registro);
        JOptionPane.showMessageDialog(null, "Se a guardado exitosamente el registro ");
        
        Limpiar();
        controller control = new controller();
        control.listarRegistro(jTable_Datos);
    }//GEN-LAST:event_jButton_GuardarMouseClicked

