    private void jButton_BusquedaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton_BusquedaMouseClicked
       String Datosrecogidos = jTextField_Busqueda.getText();
        controller buscar = new controller();
        if(jComboBox_Busqueda.getSelectedItem() == "Fecha"){
             buscar.listarFechas(jTable_Datos, Datosrecogidos);
                   
        }else if(jComboBox_Busqueda.getSelectedItem() == "Cedula"){
             buscar.listarCedula(jTable_Datos, Datosrecogidos);
                    
        }else if(jComboBox_Busqueda.getSelectedItem() == "Profesion"){
            buscar.listarprofesion(jTable_Datos, Datosrecogidos);
            
        }else{
            JOptionPane.showMessageDialog(null, "Escoja una opcion para realizar la busqueda");
        }

       
    }//GEN-LAST:event_jButton_BusquedaMouseClicked

    private void jButton_ActualizarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton_ActualizarMouseClicked
        int id = Integer.parseInt(jLabel_id.getText());
        String nombre_completo = jTextField_NombreCompleto.getText();
        String cedula = jTextField_Cedula.getText();
        int edad = Integer.parseInt(jTextField_Edad.getText());
        String direccion = jTextField_Direccion.getText();
        String telfono = jTextField_Telefono.getText();
        String profesion = jTextField_Profesion.getText();
        String estado = jComboBox_Estado.getSelectedItem().toString();
        //controlador
        controller update = new controller();
        //clase 
        registro regis = new registro();
        regis.setId(id);
        regis.setNombreCompleta(nombre_completo);
        regis.setCedula(cedula);
        regis.setEdad(edad);
        regis.setDireccion(direccion);
        regis.setTelefono(telfono);
        regis.setProfesion(profesion);
        regis.setEstado(estado);
        update.actualizar(regis);
        
        JOptionPane.showMessageDialog(null,"Se a actualizado exitosamente el registro" );
        controller contro = new controller();
        contro.listarRegistro(jTable_Datos);
        Limpiar();
    }//GEN-LAST:event_jButton_ActualizarMouseClicked

